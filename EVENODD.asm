.MODEL SMALL
.STACK 100H

.DATA
MSG1 	DB 	"THE COUNT OF EVEN NUMBERS IS = $"
MSG2 	DB 	"THE COUNT OF ODD NUMBERS IS = $"
MYARRAY DW 0001H,0002H,0003H,0004H,0014H,0006H,0007H,0008H,0009H,000AH

.CODE
MOV AX, @DATA
MOV DS, AX

XOR BX,BX
XOR DX,DX
MOV CL, 0AH
MOV SI, OFFSET MYARRAY
MOV DI, 02H
L1:
MOV AX,[SI]
SHR AX, 01
JC L2
INC BX
JMP L3
L2:
INC DX
L3:
ADD SI,2
DEC CL
JNZ L1
MOV AX,DX
PUSH 	AX

MOV 	DX, 	OFFSET MSG1
MOV 	AH,	09H
INT 	21H
POP	 AX
	
CALL 	DISPLAY

MOV AX,BX
PUSH 	AX

MOV 	DX, 	OFFSET MSG2
MOV 	AH,	09H
INT 	21H
POP	 AX
	
CALL 	DISPLAY

MOV 	AH,	4CH
INT 	21H

DISPLAY PROC NEAR ; PROC IS KEYWORD FOR PROCEDURE
MOV 	CH,	04H
MOV 	CL,	04H
DISP1:
ROL 	AX,	CL ; ROTATE LEFT  4 TIMES
PUSH 	AX ; SAVING ON STACK
AND 	AL,	0FH
ADD 	AL,	30H ; 48 IN DECIMAL
CMP 	AL,	'9' ; COMPARE WITH ASCII VALUE OF 9
JBE 	DISP2
ADD 	AL,	7
DISP2:	MOV 	DL,	AL
MOV 	AH,	02H
INT 	21H
POP 	AX
DEC 	CH
JNZ 	DISP1
;NEXT LINE CODE
MOV dl, 10
MOV ah, 02h
INT 21h
MOV dl, 13
MOV ah, 02h
INT 21h
RET
DISPLAY 	ENDP
END