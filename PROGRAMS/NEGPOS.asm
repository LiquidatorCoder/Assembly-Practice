.MODEL SMALL
.STACK 100H

.DATA
MSG1 	DB 	"THE COUNT OF NEG NUMBERS IS = $"
MSG2 	DB 	"THE COUNT OF POS NUMBERS IS = $"
MYARRAY DW 2579H, 0A500H, 0C009H, 0159H, 0B900H

.CODE

MOV AX,@DATA
MOV DS,AX

XOR BX,BX
XOR DX,DX
MOV CL, 05H
MOV SI, OFFSET MYARRAY
AGAIN: MOV AX, [SI]
SHL AX, 01
JC NEGATIVE
INC BX
JMP NEXT
NEGATIVE: INC DX
NEXT: ADD SI, 02
DEC CL
JNZ AGAIN
MOV AX,DX
PUSH 	AX

MOV 	DX, 	OFFSET MSG1
MOV 	AH,	09H
INT 	21H
POP	 AX
	
CALL 	DISPLAY

MOV AX,BX
PUSH 	AX

MOV 	DX, 	OFFSET MSG2
MOV 	AH,	09H
INT 	21H
POP	 AX
	
CALL 	DISPLAY

MOV 	AH,	4CH
INT 	21H

DISPLAY PROC NEAR ; PROC IS KEYWORD FOR PROCEDURE
MOV 	CH,	04H
MOV 	CL,	04H
DISP1:
ROL 	AX,	CL ; ROTATE LEFT  4 TIMES
PUSH 	AX ; SAVING ON STACK
AND 	AL,	0FH
ADD 	AL,	30H ; 48 IN DECIMAL
CMP 	AL,	'9' ; COMPARE WITH ASCII VALUE OF 9
JBE 	DISP2
ADD 	AL,	7
DISP2:	MOV 	DL,	AL
MOV 	AH,	02H
INT 	21H
POP 	AX
DEC 	CH
JNZ 	DISP1
;NEXT LINE CODE
MOV dl, 10
MOV ah, 02h
INT 21h
MOV dl, 13
MOV ah, 02h
INT 21h
RET
DISPLAY 	ENDP
END